# Metodos de cadenas: upper, lower, capitalize, count, find(indice de un caracter desde el principio), isdigit,
# isalum, isalpha, split, strip (elimina espacios al principio y final), replace, rfind(indice de un caracter desde
# el final r->reverse)

frase = " hola Que Tal estas  "
print(frase.capitalize())
print(frase.upper())
print(frase.lower())
print(frase.find("Q"))
print(frase.rfind("Q"))
print(frase.strip())